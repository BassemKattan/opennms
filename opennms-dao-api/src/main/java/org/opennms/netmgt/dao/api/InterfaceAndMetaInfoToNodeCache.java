package org.opennms.netmgt.dao.api;

import java.net.InetAddress;
import java.util.Optional;

public interface InterfaceAndMetaInfoToNodeCache {

    int size();

    Optional<InterfaceToNodeCache.Entry> getFirst(String location, InetAddress ipAddr);

    default Optional<Integer> getFirstNodeId(String location, InetAddress ipAddr) {
        return this.getFirst(location, ipAddr).map(e -> e.nodeId);
    }
    Optional<InterfaceToNodeCache.Entry> getFirst(String location, String context, String key, String value);


}
